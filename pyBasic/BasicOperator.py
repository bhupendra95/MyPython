pow = a**2
print(pow)



## Relational Operators
a = 24
b = 35

# a > b is False
print(a > b)

# a < b is True
print(a < b)

# a == b is False
print(a == b)

# a != b is True
print(a != b)

# a >= b is False
print(a >= b)

# a <= b is True
print(a <= b)



## Logical Operators

a = 2 > 3
b = 4 > 2

print('a & b : ',a & b)
print('a | b : ',a | b)
print ('~a : ',~a)
